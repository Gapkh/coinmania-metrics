def _latest(d):
    """Return the value for the most recent date in dict d."""
    if not d:
        return None
    return d[max(d)]


def _sum_range(d, start_iso, end_iso):
    """Sum values in dict d for dates [start_iso, end_iso) (end exclusive)."""
    total = 0
    for k, v in d.items():
        if start_iso <= k < end_iso:
            total += v
    return total


def get_android_fallback(today=None):
    """
    Compute Android install metrics from historical CSV data.
    Returns a dict suitable for merging into _android_state when GCS is unavailable.
    today: datetime.date or None (defaults to UTC today)
    """
    if today is None:
        today = datetime.now(timezone.utc).date()

    yesterday = today - timedelta(days=1)
    yesterday_str = yesterday.isoformat()
    cutoff_30 = (today - timedelta(days=30)).isoformat()
    cutoff_60 = (today - timedelta(days=60)).isoformat()
    today_str = today.isoformat()

    # active_installs: latest installed audience value
    active_installs = _latest(AND_INSTALLED)

    # total_installs: cumulative sum of all downloads (all-time)
    total_installs = sum(AND_DOWNLOADS.values())

    # installs_yesterday: downloads for yesterday (or most recent available)
    installs_yesterday = AND_DOWNLOADS.get(yesterday_str)
    if installs_yesterday is None:
        # fall back to most recent available day
        available = sorted(k for k in AND_DOWNLOADS if k < today_str)
        if available:
            installs_yesterday = AND_DOWNLOADS[available[-1]]

    # installs_30d: sum of downloads over [cutoff_30, today)
    inst_vals = {k: v for k, v in AND_DOWNLOADS.items() if cutoff_30 <= k < today_str}
    installs_30d = sum(inst_vals.values()) if inst_vals else None
    daily_installs = round(sum(inst_vals.values()) / len(inst_vals), 1) if inst_vals else None

    # installs_prev_30d: sum of downloads over [cutoff_60, cutoff_30)
    prev_vals = {k: v for k, v in AND_DOWNLOADS.items() if cutoff_60 <= k < cutoff_30}
    installs_prev_30d = sum(prev_vals.values()) if prev_vals else None

    # uninstalls_30d
    uninst_vals = {k: v for k, v in AND_UNINSTALLS.items() if cutoff_30 <= k < today_str}
    uninstalls_30d = sum(uninst_vals.values()) if uninst_vals else None
    daily_uninstalls = round(sum(uninst_vals.values()) / len(uninst_vals), 1) if uninst_vals else None

    # distinct_users (DAU): latest available
    distinct_users = _latest(AND_DAU)

    # mau: latest available
    mau = _latest(AND_MAU)

    # date that installs_yesterday refers to
    if AND_DOWNLOADS.get(yesterday_str) is not None:
        installs_yesterday_date = yesterday_str
    else:
        available = sorted(k for k in AND_DOWNLOADS if k < today_str)
        installs_yesterday_date = available[-1] if available else None

    return {
        "active_installs": active_installs,
        "total_installs": total_installs,
        "installs_yesterday": installs_yesterday,
        "installs_yesterday_date": installs_yesterday_date,
        "daily_installs": daily_installs,
        "daily_uninstalls": daily_uninstalls,
        "installs_30d": installs_30d,
        "installs_prev_30d": installs_prev_30d,
        "uninstalls_30d": uninstalls_30d,
        "distinct_users": distinct_users,
        "mau": mau,
    }


